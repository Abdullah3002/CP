#include<bits/stdc++.h>
using namespace std;

int main(){
    
   long long int n,m;
   
   cin >> n >> m;

   cout << n << " + " << m << " = " << n + m << endl;
   cout << n << " * " << m << " = " << n * m  << endl;
   cout << n << " - " << m << " = " << n - m ;
    

    return 0;
}

/*

Input: 5 10

Output: 
5 + 10 = 15
5 * 10 = 50
5 - 10 = -5

*/