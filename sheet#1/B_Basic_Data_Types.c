
#include <stdio.h>

int main() { 
   
    int a;
    scanf("%d",&a);

    printf("%d\n",a);

    long long int b;

    scanf("%lld",&b);

    printf("%lld\n",b);

    char s[101];
    scanf("%s",s);

    printf("%s\n",s);

    float c;
    scanf("%f",&c);

    printf("%.2f\n",c);

    double d;

    scanf("%lf",&d);

    printf("%.1lf",d);

   
    return 0;
}